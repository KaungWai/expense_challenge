package com.mdcr.wif;

public class GraphData {
	Float amount;
	String date;
	
	public GraphData(){}
	
	public Float getAmount() {
		return amount;
	}
	public void setAmount(Float amount) {
		this.amount = amount;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
